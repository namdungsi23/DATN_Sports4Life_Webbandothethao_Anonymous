HEADER_FILL = PatternFill("solid", fgColor="1F4E79")
HEADER_FONT = Font(bold=True, color="FFFFFF", size=11)
SUBHEADER_FILL = PatternFill("solid", fgColor="D6E4F0")
SEV_FILL = {
    "Critical": PatternFill("solid", fgColor="FF6B6B"),
    "High": PatternFill("solid", fgColor="FFA94D"),
    "Medium": PatternFill("solid", fgColor="FFE066"),
    "Low": PatternFill("solid", fgColor="B2F2BB"),
}
STATUS_FILL = {
    "Pass": PatternFill("solid", fgColor="C3FAE8"),
    "Fail": PatternFill("solid", fgColor="FFC9C9"),
    "Not Tested": PatternFill("solid", fgColor="E9ECEF"),
}
THIN = Side(style="thin", color="CCCCCC")
BORDER = Border(left=THIN, right=THIN, top=THIN, bottom=THIN)
WRAP = Alignment(wrap_text=True, vertical="top")


def style_header(ws, row, cols):
    for c in range(1, cols + 1):
        cell = ws.cell(row=row, column=c)
        cell.fill = HEADER_FILL
        cell.font = HEADER_FONT
        cell.alignment = Alignment(horizontal="center", vertical="center", wrap_text=True)
        cell.border = BORDER


def auto_width(ws, widths):
    for i, w in enumerate(widths, 1):
        ws.column_dimensions[get_column_letter(i)].width = w


