def write_sheet_summary(wb):
    ws = wb.create_sheet("Tong hop")
    ws.append(["Module", "Tổng TC", "Pass", "Fail", "Not Tested", "Bug Critical", "Bug High", "Bug Medium", "Bug Low"])
    style_header(ws, 1, 9)

    modules = sorted(set(t["module"] for t in TEST_CASES) | set(b["module"] for b in BUGS))
    for mod in modules:
        tcs = [t for t in TEST_CASES if t["module"] == mod]
        bugs = [b for b in BUGS if b["module"] == mod]
        ws.append([
            mod,
            len(tcs),
            sum(1 for t in tcs if t["status"] == "Pass"),
            sum(1 for t in tcs if t["status"] == "Fail"),
            sum(1 for t in tcs if t["status"] == "Not Tested"),
            sum(1 for b in bugs if b["severity"] == "Critical"),
            sum(1 for b in bugs if b["severity"] == "High"),
            sum(1 for b in bugs if b["severity"] == "Medium"),
            sum(1 for b in bugs if b["severity"] == "Low"),
        ])

    ws.append([])
    ws.append(["TỔNG", len(TEST_CASES),
               sum(1 for t in TEST_CASES if t["status"] == "Pass"),
               sum(1 for t in TEST_CASES if t["status"] == "Fail"),
               sum(1 for t in TEST_CASES if t["status"] == "Not Tested"),
               sum(1 for b in BUGS if b["severity"] == "Critical"),
               sum(1 for b in BUGS if b["severity"] == "High"),
               sum(1 for b in BUGS if b["severity"] == "Medium"),
               sum(1 for b in BUGS if b["severity"] == "Low")])

    auto_width(ws, [22, 10, 8, 8, 12, 14, 10, 12, 10])


